package de.aschneider.model;

public class NutzerTelefon {

	private String Key;
	private String NutTelNutStaID;
	private String NutTelNum;
	private String NutTelBez;
	
	
	
	public String getKey() {
		return Key;
	}
	public void setKey(String key) {
		Key = key;
	}
	public String getNutTelNutStaID() {
		return NutTelNutStaID;
	}
	public void setNutTelNutStaID(String nutTelNutStaID) {
		NutTelNutStaID = nutTelNutStaID;
	}
	public String getNutTelNum() {
		return NutTelNum;
	}
	public void setNutTelNum(String nutTelNum) {
		NutTelNum = nutTelNum;
	}
	public String getNutTelBez() {
		return NutTelBez;
	}
	public void setNutTelBez(String nutTelBez) {
		NutTelBez = nutTelBez;
	}
}
