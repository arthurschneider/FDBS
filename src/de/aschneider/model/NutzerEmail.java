package de.aschneider.model;

public class NutzerEmail {
	
	private String Key;
	private String NutEmaNutStaID;
	private String NutEmaAdr;
	private String NutEmaBez;
	
	
	public String getKey() {
		return Key;
	}
	public void setKey(String key) {
		Key = key;
	}
	public String getNutEmaNutStaID() {
		return NutEmaNutStaID;
	}
	public void setNutEmaNutStaID(String nutEmaNutStaID) {
		NutEmaNutStaID = nutEmaNutStaID;
	}
	public String getNutEmaAdr() {
		return NutEmaAdr;
	}
	public void setNutEmaAdr(String nutEmaAdr) {
		NutEmaAdr = nutEmaAdr;
	}
	public String getNutEmaBez() {
		return NutEmaBez;
	}
	public void setNutEmaBez(String nutEmaBez) {
		NutEmaBez = nutEmaBez;
	}
	

}
